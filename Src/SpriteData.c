// Update shooting laser object data
void Laser_Move(uint8_t speed){
	if(Laser.y > 0 && Laser.life){
		Laser.y -= speed;
	} else if (Laser.life) {
		Laser.life = 0;
	}
}

uint8_t Obj_Coll(STyp Obj0, STyp Obj1){
	uint32_t Obj0_xi = Obj0.x+1, 									Obj0_xf = Obj0.x - 1 + Obj0.image[18]; 	// Obj0 x range
	uint32_t Obj1_xi = Obj1.x, 										Obj1_xf = Obj1.x + Obj1.image[18]; 			// Obj1 x range
	uint32_t Obj0_yi = Obj0.y+1-Obj0.image[22],		Obj0_yf = Obj0.y - 2; 									// Obj0 y range
	uint32_t Obj1_yi = Obj1.y, 										Obj1_yf = Obj1.y - Obj1.image[22]; 			// Obj1 y range
	
	uint8_t x_coll = (Obj0_xi <= Obj1_xi && Obj1_xi <= Obj0_xf) || 
										(Obj0_xi <= Obj1_xf && Obj1_xf <= Obj0_xf);
	uint8_t y_coll = (Obj0_yi <= Obj1_yi && Obj1_yi <= Obj0_yf) || 
										(Obj0_yi <= Obj1_yf && Obj1_yf <= Obj0_yf);
	return (x_coll && y_coll);
}

// Update Enemy object data
void Enemy_Move(uint8_t speed){
	uint8_t i, x_colsn, y_colsn;
	for(i = 0; i < NUM_ENEMY; i++){
    if(Enemy[i].x < MAX_ENEMYX && Enemy[i].life){
      Enemy[i].x += speed;
    } else if (Enemy[i].life) {
      Enemy[i].life = 0;
      d_enemies++;
    }
		uint8_t collision = Obj_Coll(Enemy[i], Laser);
		// If enemy and Laser obj intersect, enemy object gets destroyed
		if (Enemy[i].life && Laser.life && collision){
			Enemy[i].life = 0, Laser.life = 0;
			Enemy[i].image = SmallExplosion0;
			score++;
			d_enemies++;
		}
  }
}

// Update Player object data
void Player_Move(void){
	unsigned int PlayerShipPosition;
	uint16_t adc_value;
	// Read the potentiometer value to control the spaceship's movement
	FIRFilter(&adc_value);
	// adc value sent to player ship position
	PlayerShipPosition = (adc_value * (84 - 18)) / 4095;	
	PlayerShip.x = PlayerShipPosition;
}

void Delay100ms(uint32_t count){
  unsigned long volatile time;
  while(count>0){
    time = 72724 * 5;  // 0.1sec at 80 MHz
    while(time){
      time--;
    }
    count--;
  }
}

